package com.glsx.oms.bigdata.admin.ap.monitor.model;

public class Language {
	//标示id
	private Integer id;
	//语言标示
	private Integer lanCode;
	//描述
	private String lanDes;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getLanCode() {
		return lanCode;
	}
	public void setLanCode(Integer lanCode) {
		this.lanCode = lanCode;
	}
	public String getLanDes() {
		return lanDes;
	}
	public void setLanDes(String lanDes) {
		this.lanDes = lanDes;
	}
	
	

}
