export default {
  setProgramVersion (state, data) {
    state.programVersion = data.data;
  },
  setServerLoad (state, data) {
    state.serverLoad.column = data.data.column;
    state.serverLoad.list = data.data.list;
  },
  setChannelState (state, data) {
    state.channelState.column = data.data.column;
    state.channelState.list = data.data.list;
  },
  setDeviceOnline (state, data) {
    state.deviceOnline.list1 = data.data.list1;
    state.deviceOnline.list2 = data.data.list2;
    state.deviceOnline.totalUsers = data.data.totalUsers;
  }
};
